    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        try{
           
            Class.forName("com.mysql.jdbc.Driver");
            
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/projetojava2023","root","");
            
            java.sql.Statement st = conn.createStatement();
            int cod = Integer.parseInt(id.getText());
            st.executeUpdate(" UPDATE cliente SET nome ='"+ this.nome.getText() +"' ,dataNasc = '"+ this.dataNasc.getText() + "',"
                + "cpf='"+ this.cpf.getText() + "', tel = '" +this.tel.getText()+"',"
                + "email='"+ this.email.getText() + "',bairro='"
                + this.bairro.getText() + "',rua='"+ this.rua.getText() + "',num='" + this.num.getText() + "',cidade='"+ this.cidade.getText() 
                + "'WHERE id="+cod+"");

            JOptionPane.showMessageDialog(rootPane,"Funcionario editado com sucesso");
            id.setText(null);
            nome.setText(null);
            dataNasc.setText(null);
            cpf.setText(null);
            email.setText(null);
            bairro.setText(null);
            rua.setText(null);
            num.setText(null);
            cidade.setText(null);
        }catch (SQLException | ClassNotFoundException e){
            JOptionPane.showMessageDialog(rootPane, e);
        };
    }//GEN-LAST:event_editarActionPerformed

