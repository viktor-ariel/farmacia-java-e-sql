    private void buscadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscadorActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/projetojava2023","root","");
            java.sql.Statement st = conn.createStatement();

            st.executeQuery ("select * from cliente where id = " + id.getText() + "");
            ResultSet rs = st.getResultSet();
            while (rs.next()) {
                nome.setText(rs.getString("nome"));
                dataNasc.setText(rs.getString("dataNasc"));
                cpf.setText(rs.getString("cpf"));
                tel.setText(rs.getString("cpf"));
                email.setText(rs.getString("email"));
                bairro.setText(rs.getString("bairro"));
                rua.setText(rs.getString("rua"));
                num.setText(rs.getString("num"));
                cidade.setText(rs.getString("cidade"));
            }
        }
        catch (SQLException | ClassNotFoundException e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_buscadorActionPerformed

